<h1>Reglamento</h1>

<ul>
<li>Cada usuario deberá usar la opción de autoregistro.
<li>Los marcadores pueden registrarse o modificarse hasta el mediodia del día del partido correspondiente.
    Una vez cerradas las apuestas, las apuestas registradas por todos los jugadores serán visibles para todos.</li>
<li>En caso de no registar marcador al cumplirse el límite se registrará automáticamente una apuesta aleatoria desde 0-0 hasta 4-4</li>
</ul>

<h2>Puntaje</h2>
<ul>
<li>Se podrá obtener hasta 15 puntos por cada partido.</li>
<li>Se obtienen 5 puntos si se acierta el ganador/empate (Gana Eq1, Gana Eq2 o empate)</li>
<li>Se obtienen 5 puntos por cada marcador por equipo obtenido, es decir, si la apuesta fue 2-0, y el
marcador final fue 2-1. Se obtienen 5 puntos por haber acertado el marcador del Eq1.</li>
<li>Al finalizar el torneo, se totalizarán los puntajes individuales para organizar las posiciones</li>
<li>En caso de presentarse empate en puntos entre 2 jugadores, el primer criterio de decisión será quien haya acertado
en mas ocasiones los 2 marcadores en un mismo partido, si el empate persiste el siguiente criterio será quién haya acertado
en mas ocasiones el <b>ganador/empate</b>; si el empate aún persiste, el siguiente criterio es quién haya acertado mas veces el <b>marcador del equipo 1</b>.
</ul>

<h2>Valor</h2>
<ul>
<li>Por ahora ninguno de los eventos tendrá precio...de igual manera, ninguno de los eventos contará con un premio ecnonómico para el/los jugadores en las primeras posiciones</li>
</ul>

